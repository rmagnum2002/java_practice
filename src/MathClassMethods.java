class MathClassMethods{
	public static void main(String[] args) {
		System.out.println(Math.abs(-26.7));
		System.out.println(Math.ceil(74.3));
		System.out.println(Math.floor(74.7));
		System.out.println(Math.max(10.5, 10.1));
		System.out.println(Math.min(10.5, 10.1));
		System.out.println(Math.pow(5, 3));
		System.out.println(Math.sqrt(9));
	}
}