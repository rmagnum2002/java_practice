package java_practice;
class ForLoop{
	public static void main(String[] args) {
        
		// increment by 3
		for (int i = 6; i <= 21; i+=3) {
			System.out.println(i);
		}
		System.out.println("----------------------------------");
		
		//increment by 1
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
	}
}